package org.example.wizard;

public class WizardState {
    private String createDirPath;
    private String deleteDirPath;

    public String getCreateDirPath() {
        return createDirPath;
    }

    public void setCreateDirPath(String createDirPath) {
        this.createDirPath = createDirPath;
    }

    public String getDeleteDirPath() {
        return deleteDirPath;
    }

    public void setDeleteDirPath(String deleteDirPath) {
        this.deleteDirPath = deleteDirPath;
    }
}
